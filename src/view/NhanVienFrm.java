     public final void initTable() {
        tblModel = new DefaultTableModel() ;
        String[] headerTbl = new String[]{"Mã Người Dùng", "Mã Nhân Viên", "Ngày Sinh", "Email","Giới Tính","SĐT"};
        tblModel.setColumnIdentifiers(headerTbl);
        jTable1.setModel(tblModel);
    }
     public void loadDataToTable(ArrayList<NhanVien> nv) {
        try {
            tblModel.setRowCount(0);
            for (NhanVien i : nv) {
                tblModel.addRow(new Object[]{
                    i.getMaNguoiDung(), i.getMaNhanVien(), i.getNgaySinh(), i.getEmail(), i.getGioiTinh(),i.getSDT(),i.getIsDelete()==0 ? "Xóa" : "Chưa Xóa"
                });
            }
        } catch (Exception e) {
        }
     }
